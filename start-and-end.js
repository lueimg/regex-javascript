const output = (string, regex ) => string.replace(regex, str => `(${str})`);

var str = `12/12/16 12-16-13`
output(str, /^12/g) /*? */ //(12)/12/16 12-16-13​​​​​
   
var str = `12/12/16
12-16-13`

output(str, /^12/g) /*? */
// ​​​​​(12)/12/16​​​​​
// ​​​​​12-16-13​​​​​

output(str, /^12/gm) /*? */
// ​​​​​(12)/12/16​​​​​
// ​​​​​(12)-16-13​​​​​

var str = `12/12/16
12-16-13
11/12/16
12-12-2016`

output(str, /^12/gm) /*? */
// ​​​​​(12)/12/16​​​​​
// ​​​​​(12)-16-13​​​​​
// ​​​​​11/12/16​​​​​
// ​​​​​(12)-12-2016​​​​​

output(str, /^12.+16/gm) /*? */
// ​​​​​(12/12/16)​​​​​
// ​​​​​(12-16)-13​​​​​
// ​​​​​11/12/16​​​​​
// ​​​​​(12-12-2016)​​​​​

output(str, /^12.+16$/gm) /*? */
// ​​​​​(12/12/16)​​​​​
// ​​​​​12-16-13​​​​​
// ​​​​​11/12/16​​​​​
// ​​​​​(12-12-2016)​​​​​
